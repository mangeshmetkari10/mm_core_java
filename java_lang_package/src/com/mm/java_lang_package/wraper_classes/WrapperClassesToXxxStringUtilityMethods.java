package com.mm.java_lang_package.wraper_classes;

public class WrapperClassesToXxxStringUtilityMethods {

	public static void main(String[] args) throws CloneNotSupportedException {

		System.out.println(Integer.toBinaryString(10));// 1010
		System.out.println(Integer.toHexString(10));// a
		System.out.println(Integer.toOctalString(10));// 12
		System.out.println(Long.toBinaryString(10));// 1010
		System.out.println(Long.toHexString(10));// q
		System.out.println(Long.toOctalString(10));// 12
		System.out.println(Double.toHexString(20.44));// 0x1.470a3d70a3d71p4
		System.out.println(Float.toHexString(11.5f));// 0x1.7p3

	}
}
